print(f'O resultado da soma é {s}') #Mostra o resultado da soma
print(f'O resultado da subtração é {sub}') #Mostra o resultado da subtração
print(f'O resultado da multiplicação é {multi}') #Mostra o resultado da multiplicação
print(f'O resultado da divisão é {div}') #Mostra o resultado da divisão

